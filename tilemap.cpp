void Stage::fill_coins()
{
  // Transfer any coins in the tilemap to the "coin map".
  
  for (int i = 0; i < MAP_MEM; i++)
  {
    if (pgm_read_byte(mapptr + i) == 9)
    {
      coin_map[i] = true;
    }
    else
    {
      coin_map[i] = false;
    }
  }
}

void Stage::draw_coins(uint16_t cam_offset_x)
{
  int16_t tx;
  int16_t ty;

  tile_anim++;
  if (tile_anim >= TILE_ANIM_SPEED)
  {
    tile_anim = 0;
    frame_two = !frame_two;
  }

  for (int i = 0; i < MAP_MEM; i++)
  {
    tx = ((i % MAP_WIDTH) * 8) - cam_offset_x;
    ty = (i / MAP_WIDTH) * 8;

    if (tx > -8 && tx < 128)
    {
      // For animated coins!
      if (coin_map[i] == true)
      {
        Sprites::drawOverwrite(tx, ty, foreground, 9 + frame_two);
      }
    }
  }
}

void Stage::draw_level(uint16_t cam_offset_x)
{

  int16_t tx;
  int16_t ty;

  for (int i = 0; i < MAP_MEM; i++)
  {
    if (pgm_read_byte(mapptr + i) != 0 && pgm_read_byte(mapptr + i) != TILE_COIN1)
    {
      tx = ((i % MAP_WIDTH) * 8) - cam_offset_x;
      ty = (i / MAP_WIDTH) * 8;

      if (tx > -8 && tx < 128)
      {
        Sprites::drawOverwrite(tx, ty, foreground, pgm_read_byte(mapptr + i));
      }
    }
  }
}

bool Stage::get_coin(uint16_t x, uint16_t y)
{

  if(y <= 0){
    return 0;
  }

  int i;
  uint16_t tx = x / 8;
  uint16_t ty = y / 8;

  if (ty < 0)
    return false; // Refuse to regard spaces above the screen as anything but empty.

  // Convert i to be the queried tile on the 1-d map.
  i = tx + (ty * 128);

  if(coin_map[i] == true){
    coin_map[i] = false;
    return true;
  } else {
    return false;
  }
}

bool Stage::is_solid(int16_t x, int16_t y)
{
  int i;
  uint16_t tx = x / 8;
  uint16_t ty = y / 8;

  if(y < 0){
    return 0;
  }

  if (ty < 0)
    return false; // Refuse to regard spaces above the screen as anything but empty.

  // Convert i to be the queried tile on the 1-d map.
  i = tx + (ty * 128);

  if (pgm_read_byte(mapptr + i) != TILE_EMPTY) // If not empty space...
  {
    if (pgm_read_byte(mapptr + i) <= LAST_SOLID) // If in the "solids"
    {
      return 1;
    }
    else
    {
      return 0;
    }
  }
  else
  {
    return 0;
  }
}